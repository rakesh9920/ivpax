# MEMBRANE PARAMETERS
# h_m, membrane thickness
# w_m, membrane width
# w_l, membrane length
# w_e, electrode width
# w_l, electrode length
# h_gap, vacuum gap height
# h_isol, isolation layer height
# sigma_r, relative permittivity of isolation layer

# ARRAY PARAMETERS
# nmemx, number of membranes in x dir
# nmemy, number of membranes in y dir
# memtx, which membranes are excited

# MATERIAL PROPERTIES
# e_mod, young's modulus
# nu, poisson's ratio
# rho_m, membrane density

# MEDIA PROPERTIES
# rho_fl, fluid density
# c, sound speed

# DRIVE SIGNAL PARAMETERS
# v_dc, input voltage, dc
# v_ac, input voltage, ac

# xi, modal damping coefficients
# n_tr, transformer ratio
# k_ss, spring-softening stiffness coefficients
# k, stiffness coefficients
# a_eff, effective radius of node (for self piston impedance
